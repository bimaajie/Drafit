         <h1>Tips Memilih Bibit Buah Naga Terbaik</h1>
          
            <div>
                <img src="images/imgarticle.jpg">
            </div>

            <p style="text-align: justify; text-indent: 0.5in;">
                Lagi lagi bibit menjadi syarat mutlak untuk mendapatkan hasil yang baik dengan penerapan pupuk dan media tanam yang cocok. Memilih bibit buah naga yang baik dapat memberikan efek baik juga untuk kelangsungan hidup tanaman tersebut, selain memberikan hasil yang baik, bibit buah naga yang bagus, juga akan tahan terhadap hama penyeakit dan cuaca yang kadang tidak kondusif. Pemilihan bibit yang baik biasanya di lakukan secara mandiri dengan meperhatikan poin poin sebagai berikut:
            </p>
            <p><strong>1.Bibit yang baik memiliki warna kulit batang hijau gelap</strong></p>
            <p><strong>2.Tidak ada cacat sama sekali di bagian batangnya</strong></p>
            <p><strong>3.Batang buah naga terlihat segar</strong></p>

            <p style="text-align: justify; text-indent: 0.5in;">
                Jika anda tidak sempat untuk membuat bibit buah naga endiri atau anda baru memulai pemeliharaan buah naga, sebaiknya anda mencari atau membeli bibit di tempat pembibitan yang sudah mempunyai nama baik di mata para petani buah naga, mungkin harga yang di tawarkan akan lebih mahal, tepapi perlu anda ingat, bahawa bibit yang baik akan menghasilkan keturuan dan produksi yang baik dengan di imbangi perawatan yang maksimal.
            </p>

            <p style="text-align: justify; text-indent: 0.5in;">
               Pada proses penanaman, untuk buah naga yang tidak anda ambil sendiri dari sekitar lingkungan yang ini anda tanami, sebaiknya jangang langsung di tanam, agar tidak terjadi stres. Akan lebih baik jika anda biarkan dahulu di suatu tempat dan posisinya harus di tegakkan. Fungsinya agar bibit buah naga beradaptasi terlebih dahulu sebelum di tanam. Selain itu dengan memperhatikan peradaptasian bibit buah naga, anda juga harus memberikan sedikit pupuk yang cukup, agar saat mulai penanaman, bibit tersebut tidak mengalami perubahan pada kualitasnya.
            </p>

            <p style="text-align: justify; text-indent: 0.5in;">
              Waktu yang di butuhkan untuk beradaptasi adalah sekitar 1 sampai 2 minggu, setelah bibit siap untuk di tanam, segeralah untuk menanamnya, dan jangan lupa untuk membersihkan lingkungan buah naga setiap saat, selain kelihatan rapi dan bersih, buah naga juga akan merasa di ppedulikan oleh pemiliknya.
            </p>

